<p>提供 API 接口供开发者调用的平台，基于 Spring Boot 后端 + React 前端的 <strong>全栈微服务项目</strong>。</p>
<p>管理员可以接入并发布接口、统计分析各接口调用情况；用户可以注册登录并开通接口调用权限、浏览接口、在线调试，还能使用 <strong>客户端</strong> <strong>SDK</strong> 轻松在代码中调用接口。</p>
<p>项目的前端并不复杂，更侧重后端，包含丰富的编程技巧和架构设计层面的知识。</p>
