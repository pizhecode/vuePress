<h4 id="学习意义" tabindex="-1"><a class="header-anchor" href="#学习意义"><span>学习意义</span></a></h4>
<p>1）<strong>教程资料少</strong>：网上虽然有现成的项目模板，但基本都是别人封装好的，只能按作者的要求生成，并且缺少项目教程；而本项目不仅带大家做自己的代码生成器，还会 <strong>进 2 步</strong> 扩展，打造制作自定义代码生成器的工具和平台。</p>
<p>2）<strong>新颖且亮眼</strong>：别人写代码，而你做生产代码的脚手架、工具和平台来提高研发效能，降维打击。</p>
<p>3）<strong>能学到东西</strong>：不再是增删改查的项目，而是包含了大量的实际业务场景、系统设计和优化、解决方案。</p>
<p>4）<strong>有区分度</strong>：区别于传统 Web 应用，项目涉及命令行开发、性能优化、存储优化等实战，给你的简历增加竞争力。</p>
<h4 id="解决的问题" tabindex="-1"><a class="header-anchor" href="#解决的问题"><span>解决的问题</span></a></h4>
<p>1）代码生成器本身的作用就是自动生成常见、重复性的代码片段，<strong>解决重复编码</strong>、效率低下的问题 。</p>
<p>2）虽然网上有很多代码生成器，但都是别人制作封装好的，很多时候还是 <strong>无法满足实际开发的定制化需求</strong> （比如要在每个类上增加特定的注解和注释）。这也是为什么明明有代码生成器，很多开发者还是会抱怨自己的工作总是复制粘贴、编写重复的代码、天天 CRUD（增删改查）。如果能够有一个工具帮助开发者快速定制属于自己的代码生成器，那么将进一步提高开发效率。</p>
<p>3）在团队开发中，要生成的代码可能是需要频繁变化和持续更新维护的。如果有一个线上平台来维护多个不同的代码生成器，支持在线编辑和共享生成器，在提高开发效率的同时、将有利于协作共建，打造更高质量的代码生成器。</p>
